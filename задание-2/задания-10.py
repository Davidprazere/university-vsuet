
#Пользователь вводит два числа п и к с клавиатуры. N - количество чисел из
#ряда Фибоначчи, к - порядковый номер в ряду, с которого нужно начать. Посчитай ге сумму этих чисел. В ретении этой задачи можно использовать только один цикл.

N = int(input("Введите количество чисел из ряда Фибоначчи: "))
k = int(input("Введите порядковый номер k: "))

# Инициализация первых двух чисел Фибоначчи
a, b = 0, 1

# Пропускаем k - 1 чисел, чтобы начать с числа k
for _ in range(k - 1):
    a, b = b, a + b

# Вычисляем сумму N чисел Фибоначчи, начиная с k-го
total = 0
for _ in range(N):
    total += a
    a, b = b, a + b

print("Сумма чисел:", total)